package tr.com.srdc.mdr.web.html.models;

public class DataElementMappingModel {
	private String match;
	private String mdr;
	private String matchedURI;
	public String getMatch() {
		return match;
	}
	public void setMatch(String match) {
		this.match = match;
	}
	public String getMdr() {
		return mdr;
	}
	public void setMdr(String mdr) {
		this.mdr = mdr;
	}
	public String getMatchedURI() {
		return matchedURI;
	}
	public void setMatchedURI(String matchedDE) {
		this.matchedURI = matchedDE;
	}
	
	
}
